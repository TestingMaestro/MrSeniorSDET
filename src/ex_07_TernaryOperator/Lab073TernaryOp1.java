package ex_07_TernaryOperator;

public class Lab073TernaryOp1 {
    static void main(String[] args) {

        // ? :
        // Condition ? expression 1 : expression 2

        int a = 30;
        int b = 20;

        String result = a > b ? "A is greater" : "B is Greater";
        System.out.println(result);
    }
}
