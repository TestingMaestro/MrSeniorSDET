package ex_18_Methods;

public class Lab162SimpleMethod {
    static void main(String[] args) {
        int result = f1();
        System.out.println(result);
    }

    static int f1() {
        return 20 + 20;
    }

    static double f2() {
        return 20.2 + 20.45;
    }

    static boolean f3() {
        return true;
    }

    static String f4() {
        return "Yash";
    }

    static long f5() {
        return 20 + 220;
    }
}
