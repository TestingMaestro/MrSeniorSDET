package ex_inifinityRevision;

public class Task18TraditionalSwitch
{
    static void main(String[] args) {

        char grade = 'A';
        switch (grade){
            case 'A' -> System.out.println("grade1");
            case 'B' -> System.out.println("grade1");
            default -> System.out.println("No grade");
        }
    }
}
